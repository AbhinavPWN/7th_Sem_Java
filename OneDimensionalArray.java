public class OneDimensionalArray {
    public static void main(String[] args) {
        int[] a = new int[3];
        a[0] = 45;
        a[1] = 55;
        a[2] = 65;

        // Printing a array
        System.out.println("One dimensional array are :");
        System.out.println(a[0]);
        System.out.println(a[1]);
        System.out.println(a[2]);
    }
}
